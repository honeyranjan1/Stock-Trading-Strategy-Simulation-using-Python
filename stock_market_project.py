signals = np.where(predicted_returns > 0, 1, -1)
signals = pd.Series(signals, index=price.index)
signals = signals.shift(1).fillna(0)
strategy_returns = signals * market_returns
buy_hold_returns = market_returns
strategy_cum = (1 + strategy_returns).cumprod()
buy_hold_cum = (1 + buy_hold_returns).cumprod()
initial_capital = 10000
strategy_portfolio = initial_capital * strategy_cum
buy_hold_portfolio = initial_capital * buy_hold_cum
print("Final Strategy Capital: ₹", round(strategy_portfolio.iloc[-1], 2))
print("Final Buy & Hold Capital: ₹", round(buy_hold_portfolio.iloc[-1], 2))
annual_return = strategy_returns.mean() * 252
annual_volatility = strategy_returns.std() * np.sqrt(252)
