import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.preprocessing import MinMaxScaler
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout
data = pd.read_csv("/content/drive/MyDrive/Stock_data.csv")
data['Date'] = pd.to_datetime(data['Date'])
data = data.sort_values('Date')
data.set_index('Date', inplace=True)
if 'Volume' not in data.columns:
    data['Volume'] = 0
data['MA20'] = data['Close'].rolling(window=20).mean()
def calculate_rsi(series, period=14):
    delta = series.diff()
    gain = delta.where(delta > 0, 0)
    loss = -delta.where(delta < 0, 0)
    avg_gain = gain.rolling(period).mean()
    avg_loss = loss.rolling(period).mean()
    rs = avg_gain / avg_loss
    rsi = 100 - (100 / (1 + rs))
    return rsi
data['RSI'] = calculate_rsi(data['Close'])
data.dropna(inplace=True)
features = ['Open', 'High', 'Low', 'Close', 'Volume', 'MA20', 'RSI']
data = data[features]
scaler = MinMaxScaler()
scaled_data = scaler.fit_transform(data)
sequence_length = 60
X = []
y = []
for i in range(sequence_length, len(scaled_data)):
    X.append(scaled_data[i-sequence_length:i])
    y.append(scaled_data[i][3])
X = np.array(X)
y = np.array(y)
split = int(len(X) * 0.8)
X_train, X_test = X[:split], X[split:]
y_train, y_test = y[:split], y[split:]
model = Sequential()
model.add(LSTM(64, return_sequences=True, input_shape=(X.shape[1], X.shape[2])))
model.add(Dropout(0.2))
model.add(LSTM(64))
model.add(Dropout(0.2))
model.add(Dense(1))
model.compile(optimizer='adam', loss='mean_squared_error')
model.fit(X_train, y_train, epochs=20, batch_size=32)
predictions = model.predict(X_test)
close_scaler = MinMaxScaler()
close_scaler.fit(data[['Close']])
actual_prices = close_scaler.inverse_transform(y_test.reshape(-1,1))
predicted_prices = close_scaler.inverse_transform(predictions)
plt.figure(figsize=(12,6))
plt.plot(actual_prices, label='Actual Price')
plt.plot(predicted_prices, label='Predicted Price')
plt.title("Actual vs Predicted Stock Price")
plt.legend()
plt.show()
actual_series = pd.Series(actual_prices.flatten())
predicted_series = pd.Series(predicted_prices.flatten())
actual_direction = np.where(actual_series.diff() > 0, 1, 0)
predicted_direction = np.where(predicted_series.diff() > 0, 1, 0)
actual_direction = actual_direction[1:]
predicted_direction = predicted_direction[1:]
trend_accuracy = np.mean(actual_direction == predicted_direction) * 100
print("Overall Trend Accuracy: {:.2f}%".format(trend_accuracy))
window = 10
rolling_accuracy = []
for i in range(window, len(actual_direction)):
    actual_window = actual_direction[i-window:i]
    pred_window = predicted_direction[i-window:i]
    acc = np.mean(actual_window == pred_window) * 100
    rolling_accuracy.append(acc)
rolling_accuracy = pd.Series(rolling_accuracy)
plt.figure(figsize=(12,6))
plt.plot(rolling_accuracy)
plt.axhline(y=50, linestyle='--')
plt.title("Rolling 10-Day Trend Accuracy")
plt.xlabel("Time")
plt.ylabel("Accuracy (%)")
plt.show()

